package kr.or.ddit.atta.vo;

public class AttaVO {
	private int atta_no  ;
	private String atta_nm  ;
	private String atta_path;
	private int post_no;
	
	public int getAtta_no() {
		return atta_no;
	}
	public void setAtta_no(int atta_no) {
		this.atta_no = atta_no;
	}
	public String getAtta_nm() {
		return atta_nm;
	}
	public void setAtta_nm(String atta_nm) {
		this.atta_nm = atta_nm;
	}
	public String getAtta_path() {
		return atta_path;
	}
	public void setAtta_path(String atta_path) {
		this.atta_path = atta_path;
	}
	public int getPost_no() {
		return post_no;
	}
	public void setPost_no(int post_no) {
		this.post_no = post_no;
	}
	
	
}
